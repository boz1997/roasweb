const HTML_KEYS = new Set([
  'about_title','contact_title','gallery_title','about_badge','cookie_text',
  'suit_title','suit_h2','deluxe_title','deluxe_h2',
  'family_title','family_h2','superior_title','superior_h2',
  'standart_title','standart_h2','footer_address',
  'villas_about_title','villas_gallery_title','villas_cta_title','villas_address'
]);

let currentLang = localStorage.getItem('roasLang') || 'tr';

function applyLang(lang) {
  const t = translations[lang];
  if (!t) return;
  document.querySelectorAll('[data-i18n]').forEach((el) => {
    const key = el.getAttribute('data-i18n');
    const val = t[key];
    if (val === undefined) return;
    if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
      el.placeholder = val;
    } else if (HTML_KEYS.has(key) || /<\/?[a-z]/i.test(val)) {
      el.innerHTML = val;
    } else {
      el.textContent = val;
    }
  });

  const btn = document.getElementById('langToggle');
  if (btn) {
    btn.textContent = lang === 'tr' ? 'EN' : 'TR';
    btn.setAttribute('aria-label', lang === 'tr' ? 'Switch to English' : 'Türkçeye geç');
  }
  document.documentElement.lang = lang;
  currentLang = lang;
  localStorage.setItem('roasLang', lang);
}

document.getElementById('langToggle')?.addEventListener('click', () => {
  applyLang(currentLang === 'tr' ? 'en' : 'tr');
});

if (currentLang === 'en') applyLang('en');
